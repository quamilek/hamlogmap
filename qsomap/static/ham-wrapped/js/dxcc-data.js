// Indeks DXCC po nazwie kraju (budowany leniwie)
let DXCC_BY_NAME = null;

function buildDxccNameIndex() {
    if (DXCC_BY_NAME) return;
    DXCC_BY_NAME = {};
    for (const [code, data] of Object.entries(DXCC_DATA)) {
        const name = data.name.toLowerCase();
        DXCC_BY_NAME[name] = { ...data, dxccCode: parseInt(code) };
        // Dodaj też wersję bez znaków specjalnych
        const stripped = name.replace(/[^a-z0-9]/g, '');
        if (stripped !== name) {
            DXCC_BY_NAME[stripped] = { ...data, dxccCode: parseInt(code) };
        }
    }
}

/**
 * Znajdź DXCC po nazwie kraju
 * @param {string} countryName - Nazwa kraju
 * @returns {Object|null} - Dane DXCC lub null
 */
function findDxccByName(countryName) {
    if (!countryName) return null;

    buildDxccNameIndex();

    // Próba dokładnego dopasowania
    const normalized = countryName.toLowerCase();
    if (DXCC_BY_NAME[normalized]) {
        return DXCC_BY_NAME[normalized];
    }

    // Próba bez znaków specjalnych
    const stripped = normalized.replace(/[^a-z0-9]/g, '');
    if (DXCC_BY_NAME[stripped]) {
        return DXCC_BY_NAME[stripped];
    }

    // Wyszukiwanie częściowe
    for (const [name, data] of Object.entries(DXCC_BY_NAME)) {
        if (name.includes(stripped) || stripped.includes(name)) {
            return data;
        }
    }

    return null;
}

